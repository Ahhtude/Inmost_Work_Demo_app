import UIKit

extension UIFont {
    static var robotoFont19: UIFont? {
        return UIFont(name: "Roboto-Normal", size: 19)
    }
    
    static var robotoFont28: UIFont? {
        return UIFont(name: "Roboto-Normal", size: 28)
    }
}
